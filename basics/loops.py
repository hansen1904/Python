# Loops
i = 1
while i <= 10:
    print(i)
    i += 1

print("End of While Loop")
print()

for letter in "Magnus Hansen":
    print(letter)

print("End of For Loop")
print()

friends = ["Magnus", "Anders", "Andreas"]
for index in range(len(friends)):
    print(friends[index])
print("End of Friends Loop \n")
